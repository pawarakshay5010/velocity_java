package control_statement;

public class example_switch_6   
{
public static void main(String[] args) 
{
	
	
	String name= "akshay";
	switch(name)
	{
	
	case "harshjeet": System.out.println("running name harshjeet");
	break;
	
	case "harsh": System.out.println("running name harsh");
	break;
	
	case "sumit": System.out.println("running name sumit");
	break;
	
	case "ajay": System.out.println("running name ajay");
	break;
	
	case "akki": System.out.println("running name akki");
	break;
	case "akshay": System.out.println("running name akshay ");
	break;
	
	
	default : System.out.println("invalid name");
	
	

	
	}
	
	
	
}
}
