package control_statement;

public class example_switch_9
{
public static void main(String[] args)
{
	int number=3;
	switch(number)
	
	{
	
	case 1: System.out.println("MULADHARA CHAKRA");
	break;
	
	case 2: System.out.println("SWADHISTANA CHAKRA");
	break;
	
	
	case 3: System.out.println("MANIPURA CHAKRA");
	break;
	
	
	case 4: System.out.println("ANAHATA CHAKRA");
	break;
	
	case 5: System.out.println("VISSUDHI CHAKRA");
	break;
	
	case 6: System.out.println("AJNA CHAKRA");
	break;
	
	case 7: System.out.println("SAHASTRAS CHAKRA");
	break;
	
	default: System.out.println("invalid");
	
	
	
	
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
