package control_statement;

public class example_switch_7 
{
public static void main(String[] args) 
{
	
	int input=8;

	switch(input)
	{
	
	case 1: System.out.println("APPLE");
	break;
	
	case 2: System.out.println("SAMSUNG");
	break;
	
	case 3: System.out.println("SONY");
	break;
	
	case 4: System.out.println("ONEPLUS");
	break;
	
	case 5: System.out.println("LG");
	break;
	
	case 6: System.out.println("HONOR");
	break;
	
	case 7: System.out.println("MOTO");
	break;
	
	default : System.out.println("INVALID INPUT");
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
}
