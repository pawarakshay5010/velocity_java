package control_statement;

public class example_switch_4
{
public static void main(String[] args)
{
	int input=7;
	
	switch(input)
	{
	
	case 1: System.out.println("apple");
	break;
	case 2: System.out.println("orange");
	break;
	
	case 3: System.out.println("banana");
	break;
	case 4: System.out.println("mango");
	break;
	
	case 5: System.out.println("strawberry");
	break;
	
	
	case 6: System.out.println("lichy");
	break;
	
	case 7: System.out.println("sweetlemon");
	break;
	
	default : System.out.println("pear");
	
	
	}
	
	

	
	
}
}
