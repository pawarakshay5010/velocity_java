{
	
	int b=7;

	if(b>=5)
	{
		System.out.println("EXCELLENT");
	}
	
	else if(b>=4)
	{
		System.out.println("VERY GOOD");
	}
	
	else if(b>=3)
	{
		System.out.println("GOOD");
	}
	
	else if(b>=2)
	{
		System.out.println("CRITICAL");
	}
	
	else if(b>=1)
	{
		System.out.println("VERY CRITICAL");
	}
	
	else
	{
		System.out.println("HYPER CRITICAL");
	}
	
	
	
}
