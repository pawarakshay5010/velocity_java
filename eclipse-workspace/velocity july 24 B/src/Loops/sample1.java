package Loops;

public class sample1
{
public static void main(String[] args)                               // loops are used for repetative operations
{
	                                                                //   types: 1)for loop
	for(int i=5;  i<=50;  i=i+5)               // for loop         //           2)while loop
	{                                                              //          3)do_while loop
                                                                   //          4)for each loop
		System.out.println(i);
	}
	
	
	{
		System.out.println(".....................................................................................................................");
	}
	
	
	int a=1;                                    //while  loop
	
	while(a<=7)
	{
		System.out.println(a);
		
		a++;
	}
	
	
	{
		System.out.println(".............................................................................................................................");
	}
	
	
	
	int b=11;                                    // do_while loop
	
	do
	{
		System.out.println(b);
		
		b=b+3;
		
		
	}
	
	while(b<=20);
	
	
}
}
