package loops;

public class all_loops 
{
public static void main(String[] args) {
	
	
	
	
	//for
	
	for(int i=100; i<=110; i++)
	{
		
		
		
		System.out.println(i);
	}
	
	
	System.out.println("....................................");
	
	
	//while
	
	int j=0;
	
	while(j<=5)
	{
		
		System.out.println(j);
		
		j++;
	}
	
	
	System.out.println("....................................");
	
	
	int k=500;
	
	do
	{
		
		System.out.println(k);
		k--;
		
	}
	
	
	while(k>=490);
	
	
	System.out.println("....................................");
	
	
}
}
